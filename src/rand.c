#include <stdlib.h>

static unsigned long wcrt_rand_next = 1;

void srand(size_t seed)
{
    wcrt_rand_next = seed;
}

int rand(void)
{
    wcrt_rand_next = wcrt_rand_next * 1103515245 + 12345;
    return (wcrt_rand_next >> 16) & 0x7fff;
}
